package operadores;

public class Aula4 {

	public static void main(String[] args) {

		int num = 5;
		
		System.out.println(--num);
		
		System.out.println(++num);
		
		System.out.println(++num);
		
		boolean ligado = true;
		System.out.println(ligado);
		
		ligado = !ligado;
		System.out.println(ligado);
	}
}
